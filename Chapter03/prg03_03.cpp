#include <iostream>
using namespace std;
int main() {
    int x = 4;
    int y = -10;

    cout << "x에 양수 연산자 적용하기 : " << +x << endl;
    cout << "x에 음수 연산자 적용하기 : " << -x << endl;

    cout << "y에 양수 연산자 적용하기 : " << +y << endl;
    cout << "y에 음수 연산자 적용하기 : " << -y << endl;

    return 0;
}